#include <iostream>

void main() {
	int a, b, c, d, e;

	a = 9 + 9;
	b = 9 - 9;
	c = 9 * 9;
	d = 9 / 9;
	e = 9 % 9;

	std::cout << "足し算" << a << std::endl;
	std::cout << "引き算" << b << std::endl;
	std::cout << "掛け算" << c << std::endl;
	std::cout << "割り算" << d << std::endl;
	std::cout << "余り" << e << std::endl;
}